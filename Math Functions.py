import math

pi = 3.14

print(round(pi)) # To get the nearest number output 3
print(math.floor(pi)) # To the nearest descending number output  3
print(math.ceil(pi)) # To the nearest ascending number output 4
print(abs(pi)) # To take the number positive output 3.14
print(pow(5,2)) # output 25
print(math.sqrt(3.14)) # to radical number output 1.772004514666935
print(max(2,5,9)) # To take the largest number output 9