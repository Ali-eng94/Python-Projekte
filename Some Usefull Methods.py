name = "Ali Haji"

print(len(name)) #How many characters are in the variable output 8
print(name.find("l"))#How many l characters are in the variable output 1
print(name.capitalize())#the first letter will become capital output Ali haji
print(name.upper())#All characters in the variable will become capital letter output ALI HAJI
print(name.lower())#All characters in the variable will become lowercase output ali haji
print(name.count("a"))#How many similar characters a are there in this variable output 1
print(name.replace("a","o"))#Replace this letter with another letter output Ali Hoji

