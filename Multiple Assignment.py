import math

pi = 3.14

print(round(3.14))              # Rounds to the nearest whole number
print(math.ceil(3.14))          # Always rounds up
print(math.floor(3.14))         # Always rounds down
print(abs(pi))                  # Absolute value of pi
print(pow(5,2))                 # 5 to the power of 2
print(math.sqrt(25))            # Square root of 25
print(max(2,5,9))               # Largest number


